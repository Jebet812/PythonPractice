print("Integers")

def Integers():
    print("\nAddition")
    result = 8 + 12
    print(result)
    
    print("\nSubtraction")
    result = 12 - 8
    print(result)
    
    print("\nMultiplication")
    result = 8 * 12
    print(result)
    
    
    print("\nDivision")
    result = 12 / 4
    print(result)
    
    
    print("\nExponents")
    result = 8 ** 12
    print(result)
    
    
    print("\nMixed Operation")
    result = 2 + 4 * 6
    print(result)
    
Integers()  


print("Floats")  

