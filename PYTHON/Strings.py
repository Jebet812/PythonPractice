def Alluppercase():
    print("\nChange String to all uppercase")
    name = "cynthia jebet"
    print(name.upper())
    
Alluppercase()

def Alllowecase():
    print("\nChange String to all lowercase")
    name = "cynthia jebet"
    print(name.lower())
    
Alllowecase()

